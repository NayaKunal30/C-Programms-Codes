// WAP TO CHECK EVEN OR ODD FOR A NUMBER
#include<stdio.h>
int main ()
{
    int x;
    printf("enter the number to check = ");
    scanf("%d" ,&x);
    if (x%2==0)
    {
        printf("the number is even ,\n");
    }
    else
    {
        printf("the number is odd ,\n");
    }
    return 0 ;

}
